    const jobTitleInput = document.getElementById("jobTitleInput");
    const jobResults = document.getElementById("jobResults");
    const companyInfo = document.getElementById("companyInfo");
    const companyDetails = document.getElementById("companyDetails");

    // Display job titles matching the search input
    jobTitleInput.addEventListener("input", () => {
        const searchTerm = jobTitleInput.value.toLowerCase();
        const filteredJobs = jobData.filter(job => job.title.toLowerCase().includes(searchTerm));
        displayJobs(filteredJobs);
    });

    // Display companies related to the selected job title
  jobResults.addEventListener("click", (event) => {
        const selectedJob = jobData.filter(job => job.title.toLowerCase().includes(event.target.textContent.toLowerCase()));
        displayCompanyInfo(selectedJob[0]);
    });

    function displayJobs(jobs) {
        jobResults.innerHTML = "";
        jobs.forEach((job, index) => {
            const li = document.createElement("li");
            li.textContent = job.title;
            li.setAttribute("data-index", index);
            jobResults.appendChild(li);
        });
    }

    function displayCompanyInfo(job) {
        companyDetails.innerHTML = "";
        job.companies.forEach(company => {
            const div = document.createElement("div");
            div.classList.add("company");
            div.innerHTML = `
                <a href="${company.website}" target="_blank">
                    <img src="${company.logo}" alt="${company.name} Logo">
                </a>
                <div class="company-details">
                    <h3>${company.name}</h3>
                    <p>Location: ${company.location}</p>
                    <p>Description: ${company.description}</p>
                </div>
                <div class="visit-website">
                    <a href="${company.website}" target="_blank">Visit Website</a>
                </div>
            `;
            companyDetails.appendChild(div);
        });
        companyInfo.style.display = "block";
    }
